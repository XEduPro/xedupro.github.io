var currentLang = localStorage.getItem("lang") || "zh-CN";

function t(key) {
  var langData = i18nData[currentLang];
  if (langData && langData[key] !== undefined) {
    return langData[key];
  }
  var fallback = i18nData["zh-CN"];
  return fallback[key] || key;
}

function applyLang() {
  var elements = document.querySelectorAll("[data-i18n]");
  elements.forEach(function (el) {
    var key = el.getAttribute("data-i18n");
    var text = t(key);
    if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
      el.setAttribute("placeholder", text);
    } else {
      el.innerHTML = text;
    }
  });

  document.querySelectorAll("[data-i18n-title]").forEach(function (el) {
    el.setAttribute("title", t(el.getAttribute("data-i18n-title")));
  });

  var htmlEl = document.documentElement;
  if (currentLang === "zh-CN") {
    htmlEl.setAttribute("lang", "zh-CN");
  } else if (currentLang === "zh-TW") {
    htmlEl.setAttribute("lang", "zh-TW");
  } else {
    htmlEl.setAttribute("lang", "en");
  }

  var langLabel = document.getElementById("lang-label");
  if (langLabel) {
    langLabel.textContent = t("lang.label");
  }
}

function switchLang(lang) {
  currentLang = lang;
  localStorage.setItem("lang", lang);
  applyLang();
}

document.addEventListener("DOMContentLoaded", function () {
  applyLang();
});
